def somar(primeiro: float, segundo: float): # so pra ter certeza que os numeros sao floats
    return (primeiro+segundo)               # retorna o resultado da operacao


def mult(primeiro: float, segundo: float):
    return (primeiro*segundo)


def dividir(primeiro: float, segundo: float):
    return (primeiro+segundo)


def potenc(primeiro: float, segundo: float):
    return (primeiro ** segundo)
